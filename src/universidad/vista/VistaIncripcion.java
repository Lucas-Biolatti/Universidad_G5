    public void cargaAlumnos(){
        List <Alumno> lista = ad.obtenerAlumnos();
        for(Alumno a:lista){
            cbAlumno.addItem(a);
        }
    }
    public void armarCabecera(){
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("ID");
        columnas.add("Nombre");
        
        for(Object i:columnas){
        modelo.addColumn(i);
        }
        tabla.setModel(modelo);
    }
    public void borrarFilas(){
    
        int a = modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
            
        }
    }
    public void cargarInscriptas(){
        borrarFilas();
        
        Alumno seleccionado = (Alumno) cbAlumno.getSelectedItem();
        List<Materia> lista=id.obtenerMateriasCursadas(seleccionado.getIdAlumno());
            for(Materia m:lista){
                modelo.addRow(new Object[]{m.getIdMateria(),m.getNombreMateria()});
            }
    }
    public void cargarNoInscriptas(){
        borrarFilas();
        Alumno seleccionado = (Alumno) cbAlumno.getSelectedItem();
        List<Materia> lista=id.obtenerMateriasNoCursadas(seleccionado.getIdAlumno());
        for(Materia m:lista){
                modelo.addRow(new Object[]{m.getIdMateria(),m.getNombreMateria()});
            }
        
    
    }

