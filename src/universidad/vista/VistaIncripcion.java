    private void bInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInscribirActionPerformed
        // TODO add your handling code here:
        int fSel=tabla.getSelectedRow();
        if(fSel !=-1){
        Alumno a=(Alumno) cbAlumno.getSelectedItem();
        int idMateria=(int) modelo.getValueAt(fSel,0);
        String nomMat=(String) modelo.getValueAt(fSel,1);
        
        Materia m=new Materia(idMateria,nomMat);
        
        Inscripcion i=new Inscripcion(a,m,0);
        id.guardarInscripcion(i);
        borrarFilas();
        }
    }//GEN-LAST:event_bInscribirActionPerformed

